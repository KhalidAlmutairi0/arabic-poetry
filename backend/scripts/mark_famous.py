async def main():
    async with Session() as s:
        before = (await s.execute(text(
            "SELECT COUNT(*) FROM verses WHERE is_famous = true"
        ))).scalar()
        print(f"Famous verses before: {before}")

        total_marked = 0

        for fragment in FAMOUS_FRAGMENTS:
            # Match against normalized column (no diacritics)
            result = await s.execute(text("""
                UPDATE verses
                SET is_famous = true
                WHERE full_verse_normalized LIKE :pattern
                  AND is_famous = false
            """), {"pattern": f"%{fragment}%"})
            cnt = result.rowcount
            if cnt > 0:
                print(f"  +{cnt:2d}  {fragment[:45]}")
                total_marked += cnt

        await s.commit()

        after = (await s.execute(text(
            "SELECT COUNT(*) FROM verses WHERE is_famous = true"
        ))).scalar()
        print(f"\n✅ Marked {total_marked} additional verses as famous")
        print(f"   Total famous now: {after}")

        # Show sample of newly famous
        sample = (await s.execute(text("""
            SELECT full_verse, poet_name_ar
            FROM verses
            WHERE is_famous = true
            ORDER BY RANDOM()
            LIMIT 20
        """))).fetchall()
        print("\nSample famous verses:")
        for r in sample:
            print(f"  [{r.poet_name_ar}] {r.full_verse[:80]}")


if __name__ == "__main__":
    asyncio.run(main())
